<h1>The pH of Soil</h1>
<h2>Background:</h2>
<p>
  Depending on the type of plant, the parent material of the soil, and the
  amount of rainfall in the area, the pH of a soil can be anywhere from 3 (very
  acidic) to 10 (alkaline). However, in the past few decades we&#39;ve seen a
  systemic shift of all soils becoming more acidic, leading to unlivable
  habitats for the microorganisms within.
</p>
<h2 id="the-four-major-reasons-for-acidic-soil-are-">
  The four major reasons for acidic soil are:
</h2>
<p>
  <strong>Rainfall and Leaching:</strong>
  As rainfall increases with extreme conditions like flash flooding, the process
  of leaching occurs, where salts and nutrients are washed away from the surface
  of the soil. These salts, like calcium, magnesium, sodium, and potassium, are basic
  and prevent soil acidity. As they disappear, the pH of the soil gets more acidic.
</p>
<p>
  <em>Possible Solution:</em> Implement techniques to prevent water runoff, like
  adding conservation buffers (ex. hedgerows), improving root penetration, and improving
  soil quality to maximize absorption.
</p>
<p>Note: this problem is exacerbated in wet climates</p>
<p>
  <strong>Harvest of High-yielding Crops</strong>
  During growth, crops absorb basic elements such as calcium, magnesium, and potassium
  to satisfy their nutritional requirements. As crop yields increase, more of these
  limelike nutrients are removed from the field. During growth, crops absorb basic
  elements such as calcium, magnesium, and potassium to satisfy their nutritional
  requirements. As crop yields increase, more of these limelike nutrients are removed
  from the field.
</p>
<p>
  If plants must accumulate nutrients, and we must harvest the plants, how can
  we add nutrients back into the soil? Do we have to use additional synthetic
  inputs, fertilizer, or compost?
</p>
<p>
  An alternative to synthetic inputs is crop rotation and biodiversity. Some
  plants take up different nutrients than others. For example, nightshades
  (tomatoes, eggplants, peppers) require a lot of nitrogen and phosphorus to
  produce their fruits – like tomatoes. Root crops – like parsnips – on the
  other hand, take up a lot of calcium and potassium. Legumes are famous for
  their ability to add nitrogen back into the soil (<a
    href="https://joegardener.com/podcast/crop-rotation-basics-and-beyond/"
    >learn more about how</a
  >). Planting nightshades in the same spot, year after year, will completely
  deplete the soil of nitrogen and phosphorous. However, rotating the crops by
  planting nightshades one year and root crops another year allows the soil to
  recover the depleted nutrients while growing other vegetables. There are
  specific combinations of families that work best when rotated together.
  <img
    src="https://res.cloudinary.com/djp21wtxm/image/upload/v1668758541/i960x960-fNIWxbls0iwD_qfn8ad.png"
  />
</p>
<p>
  <strong>Organic Matter Decay</strong>
  The decay of organic matter naturally produces hydrogen ions, which are responsible
  for acidity. This is insignificant in the short term, but when accumulated over
  years without balancing mechanisms, can alter the health of the soil.
</p>
<p>
  <strong>Nitrogen Fertilizers</strong>
  Some nitrogen fertilizers contain ammonium-N. As the ammonium-N in fertilizers
  undergoes nitrification (conversion of ammonium to nitrate in soils by bacteria),
  hydrogen (H+) is released, increases acidity. As the percentage of ammonium increases
  in a given fertilizer the acidifying potential will also be increased, thus reducing
  pH.
</p>
<p>
  <strong>Acidic Parent Material</strong>
  Soils that are developed from weathered granite are likely to be more acidic than
  those developed from shale or limestone, due to the natural mineral composition
  of granite.
</p>
<p>
  <em>Possible Solution:</em> Mix soil sources (some from granite, some from limestone)
  to balance pH levels and mineral composition. Other sources include weathered bedrock,
  till, outwash deposit, eolian sand, loess, alluvium, and local overwash.
</p>
<h1 id="why-is-acidity-so-bad-">Why is acidity so bad?</h1>
<p>It limits the availability of some plant nutrients.</p>
<p>
  Increases the soil solution&#39;s toxic elements, such as aluminum and
  manganese
</p>
<p>
  Toxic materials are the major cause of poor crop performance and failure in
  acidic soils
</p>
<p>
  Too much aluminum can limit or stop root development, and plants cant absorb
  water and nutrients (<a
    href="https://www.noble.org/news/publications/ag-news-and-views/1999/january/understanding-and-correcting-soil-acidity/#:~:text=Soil%20acidity%20can%20be%20corrected,relatively%20easy%20to%20manage%20source."
    >source</a
  >))
</p>
<p>
  Too much manganese interfere with normal growth processes in the aerial plant
  parts, which stunts the plant, discolors it, and causes poor yields (<a
    href="https://www.noble.org/news/publications/ag-news-and-views/1999/january/understanding-and-correcting-soil-acidity/#:~:text=Soil%20acidity%20can%20be%20corrected,relatively%20easy%20to%20manage%20source."
    >source</a
  >))
</p>
<p>
  The more acidic our soil gets, the less vegetables are able to grow and absorb
  nutrients, <strong>making our vegetables less nutrient rich</strong>. This is
  part of the reason why 1 cup of broccoli from 20 years ago has the same amount
  of nutrients as 3 cups of broccoli now. We aren’t eating 3x more broccoli.
</p>
<h1 id="supplements">Supplements</h1>
<p>
  Limestone is the most commonly used pH balancer because of its relative
  abundance, ease of extraction, and water insolubility. It is estimated that
  roughly <a
    href="http://vegetablegrowersnews.com/article/lime-can-improve-your-bottom-line/"
    >20 percent of North American farm fields are acidic</a
  >
  and will need roughly <strong>2 tons of agricultural lime per acre</strong> in
  order to supplement the acidity.
</p>
<p>
  <strong
    >Farmers would save so much money in the long term by removing the need for
    limestone and making their soils pH more sustainable.</strong
  >
</p>
<blockquote>
  <p>
    According to the Quality Lime Company, they’d save 22.5$ per acre in
    agricultural limestone.
  </p>
</blockquote>
<p>
  The average farm in the US is 445 acres. That’s a total of <strong
    >$10,013 per year in limestone, because of acidic soil.</strong
  >
</p>
<p>
  This doesn’t even factor in the costs of nitrogen fertilizers and other inputs
  farmers are using to balance the pH of their soil and add minerals back.
</p>
<p>
  Depending on the kind of soil you have (sand or clay), you’ll require
  different amounts of limestone to have the same effect. Clay soils require
  more lime to achieve a favorable ph, and due to the results of conventional
  agriculture practices, more of our soil is turning into clay (<a
    href="https://www.thespruce.com/understanding-and-improving-clay-soil-2539857"
    >learn more here</a
  >). At this rate, expenses for lime and other pH balancers are going to
  skyrocket.
</p>
<h1 id="quick-takeaways-">Quick Takeaways:</h1>
<p>Whats the optimal pH of soil? <strong>neutral, between 6.5-7.5.</strong></p>
<p>
  What methods are current farmers using to counteract acidity? <strong
    >nitrogen fertilizers, limestone, soil conditioners</strong
  >
</p>
<p>
  How can we create more long term solutions to soil acidity, while minimizing
  cost? <strong
    >implement crop rotation, maximize biodiversity of plants and microorganisms
    in the soil, and improve soil quality</strong
  >
</p>
