<h3 id="cover-cropping-mainly-vetch-and-barley-">
  Cover cropping: (mainly vetch and barley)
</h3>
<p>
  This is the simplest way to build soil organic matter (carbon). Cover cropping
  is growing a crop to disc into the soil that increases the soil carbon. This
  additional organic matter is feed for the millions of microbes and other
  critters that live in the soil and allows them to thrive. They in turn play a
  critical role in decomposing this plant matter and making the nutrients it
  contains available to subsequent crops.
</p>
<h3 id="returning-of-crop-residue-to-soil-">
  Returning of crop residue to soil:
</h3>
<p>
  After harvest, we return all remaining parts of the plant back to the soil to
  become food for the soil microbiological community. This practice also results
  in increased soil carbon.
</p>
<h3 id="maintain-conditions-conducive-to-microbiological-health-">
  Maintain conditions conducive to microbiological health:
</h3>
<p>
  Maintain soil moisture, proper PH, and protection from sun and harsh weather
</p>
<h3 id="crop-rotation-">Crop rotation:</h3>
<p>
  Proper crop rotation breaks up disease cycles as pests fond of a certain crop
  are interrupted and allows time for soil to build up nutrients previously use
  by last crop
</p>
<h3 id="minimum-tillage-">Minimum Tillage:</h3>
<p>
  Soil disruption can be harmful to micro-biotic soil communities. By keeping
  tillage practices to a minimum, we can be less disruptive while also reducing
  soil compaction and erosion.
</p>
