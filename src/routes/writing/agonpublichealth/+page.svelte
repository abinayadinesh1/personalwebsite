  crops are up to 38% lower than there were at the middle of the 20th Century.
  This occurred after the major industrialization that occurred in WWII, and
  people wonder if there are connections between the increased use of artificial
  pesticides, fertilizers and other chemicals and food we eat. The Broadbalk
  experiment, started in 1843, has been comparing the effect of inorganic
  [artificial] fertilisers and organic manures on winter wheat. It has
  specifically examined the levels of iron and zinc in wheat grown under
  different farming methods. One of their results is although the soil contains
  the same amount of micronutrients, the nutritional profile of the produce is
  still decreasing. Another scientist, Norman Borlaug, discovered a method to
  reduce the stalk height of wheat by 20%. Being shorter, the plant was able to
  pump more starches into the the spike, where the wheat grains grow. Resulting
  wheat flour therefore had a disproportionate amount of starch compared to
  nutrients. While the Green Revolution helped to tackle world hunger, today we
  find ourselves with a global food system that in some cases has been designed
  to deliver calories and cosmetic perfection but not necessarily nutrition.
  This is contributing to a phenomenon called hidden hunger, where people feel
  sated but may not be healthy, as their food is calorie-rich but nutrient-poor
  (source). This story, by BBC, depicts a history of studies surrounding the
  link between soil and produce nutritional profiles. My theory is, as an
  effect, people need more vegetables to get the same nutrition, which pushes
  the demand for veggies, which pushes farmers to minimize costs while
  maximizing output using suboptimal methods, making food less nutrient rich and
  making climate change worse. The cycle repeats itself in every way possible.
