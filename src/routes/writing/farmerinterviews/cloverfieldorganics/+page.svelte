<h2 id="takeaways">Takeaways</h2>
<p>
  This farm is beautiful. A living, breathing manifestation of all that is
  beautiful in the world. Clear by the way they treat their customers, assign
  emotions and personify their plants + animals, and are so altruistic in their
  day to day activities.
</p>
<p>
  Obviously, this isn&#39;t scalable. It doesn&#39;t make much profit. But,
  it&#39;s clearly an enjoyable, fulfilling way to live out your years and
  provides much happiness and sustenance to the familiies who are lucky enough
  to stop by.
</p>
