<h2 id="general">General</h2>
<p>
  <strong>A bit about the farm:</strong>
  Cloverfield Organic has been operating for a decade, as an all-organic, u-pick
  farm. They are located near a residential area and are simply fenced off with dear
  fencing. A tactic they use to ward wildlife is to plant flowers in front of the
  fencing so the deers eat the flowers, rather than the vegetables.
</p>
<p>
  <strong>How does your U-Pick farm operate?</strong>
  They run friday - sunday, from 12-5pm. The rest of the time is needed for the farm
  to breathe, regenerate.
</p>
<p>
  <strong>Their philosophy</strong> is for people to take 6 for the belly, one for
  the basket. People come there to relax - as they are in the middle of a quiet,
  neighborhood. Many are surprised that a farm even exists there! The produce is
  relatively cheap (he says) - a grocery bag full is 36$,and he himself is paid mainly
  through smiles.
</p>
<p>
  <em>Aside:</em> This has me wondering a LOT about how this operational is profitable.
  My answer is that it really isn&#39;t, meaning that it also isn&#39;t scalable
  or juicy to the normal farmer who needs to make money.
</p>
<p>
  <strong>What kind of crops do you grow?</strong>
  Herbs including strawberry mint, apple mint, and regular mint. Greens including
  rhubarb, tomatoes, arugula, and collard greens. In the spring, 4 types of mustards
  that range in spiciness. The owners also keep a small sunflower farm elsewhere.
</p>
<p><strong>Do you grow anything other than crops?</strong></p>
<p>
  They have a private beekeeper who maintains 14 beehives on their land. He
  brings his bees there to make them super healthy, then makes deliciious both
  the beekeeper and the farm can sell.
</p>
<p>
  Michael offers insight through an analogy to this: Oatmeal is okay, but if
  someone had it forever they&#39;d be sick of it. In Sam Valley, nearby, there
  are a lot of almond orchards. Bees get sick of pollinating the same food every
  day, whereas they thrive in the diverse food forest that is Cloverfield
  Organics.
</p>
<p>
  Note: Michael explains this as a mutualistic relationship between them and the
  beekeeper, and not much of an income stream.
</p>
<h2 id="costs">Costs</h2>
<p>
  <strong>What do your finances look like?</strong>
  They aren&#39;t pulling in thousands and thousands of dollars. Michael is often
  (literally) paid by the smiles of his customers, and neither him nor Susan have
  a strong will to turn this into a profit-machine.
</p>
<h2 id="soil">Soil</h2>
<p>
  <strong
    >What are some strategies/design choices you use to improve the quality of
    your soil?</strong
  >
</p>
<ol>
  <li>
    Michael&#39;s main philosophy is that if you sit back, with no intervention,
    and let the plant do it&#39;s thing, the soil will be healthiest. He has
    minimal day to day upkeep, apart from watering the plants. His exact words
    were to, &quot;stick a seed in the ground and watch it get huge&quot;.
  </li>
  <li>
    They add woodchips to augment the soil, meaning, add carbon back to it. A
    huge pile of woodchips gets dropped (from where?) and they spread them
    around the farm grounds.
  </li>
  <li>
    Compost is constantly incorporated. Weeds are just future compost, and any
    plant material not sold is also turned into compost.
  </li>
  <li>
    In terms of soil composition, they started with clay soil, woodchips, and
    augmentated it with w fedather meal, alfalfa, oyster shells, and more. He
    speaks of these additions like they are obvious.
  </li>
  <li>
    They make the beds, at maximum, 5 feet wide so they can easily reach them.
    Michael says, &quot;plants need water + air in the roots, so you can&#39;t
    stand on them.&quot; I wonder how the soil on conventional agriculture farms
    are impacted by the heavy machinery that is rolled over them every season.
  </li>
  <li>Crop Rotation!!</li>
  <li>
    Michael explains how fava beans add nitrogen to the soil, so they alternate
    it with plants that require more.
  </li>
  <li>
    They will plant specific families together (ex. corn, beans, squash) to
    balance out their nutrient necessities. He explains how when they plant
    corn, the beans grows up the corn stalks, and the squash nearby starts
    blooming like crazy, so it all works together in perfect harmony.
  </li>
  <li>They try to maximize their dirt surface (stagger seeds).</li>
  <li>
    Michael goes over the same spot with water multiple times, explaining that
    &quot;if you throw water on one spot, the ground will seal up. But if you go
    over the same spot multiple times, it relaxes and soaks it all in&quot;. It
    almost sounds as if the first time water is added, the plant stressfully
    tries to grab as much as it can and hold it close to the surface. Whereas
    when he goes back and forth, the plants relax and let the water go deep into
    their roots.
  </li>
</ol>
