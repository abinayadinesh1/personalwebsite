<h1>Ratto Brothers</h1>
<p><a href="https://www.rattobros.com/">Website</a></p>
<p>Location: Central Valley in CA</p>
<p>
  <em
    >Talked to Frank. Content below is a mixture of information from their
    website, and a phone call with him.</em
  >
</p>
<h2 id="general">General</h2>
<p>
  <strong>When did this farm begin?</strong>
  His great grandpa started it in 1905, and his family has been running it since.
</p>
<p>
  <strong>What kind of farm do you run?</strong> Mainly a non-organic farm, with
  a few select plots growing organic produce. This is mainly because &quot;organics
  are difficult to grow in central valley because heat brings bugs and weeds&quot;.
  Although he can sell them at a higher price, it is too expensive to harvest if
  the quality is not up to par.
</p>
<p>Sometimes, they do produce organic organic kale, leaf lettuce and leeks.</p>
<p>
  <strong>What are your main crops?</strong>
  They are known nationally as a leafy greens grower, varieties including collards,
  mustards, turnips, herbs, asian veggies, and cabbages.
</p>
<p>
  <strong>What practices do you use to improve your farm?</strong>
  Frank&#39;s general philosophy is to &quot;be a good steward to the environment&quot;.
  For the farm, this means:
</p>
<ul>
  <li>implementing fertility programs</li>
  <li>
    adding organic waste back into the ground (including whatever is left over
    from harvesting)
  </li>
  <li>
    limiting fertilizers, herbicides and pesticides (they are &#39;expensive and
    bad&#39;)
    <ul>
      <li>
        how many plots of land are you operating? what kinds of crops do you
        grow?
      </li>
    </ul>
  </li>
</ul>
<p><strong>How do you preserve your goods?</strong></p>
<p>
  “Post-harvest research has shown that every hour between harvest and cooling
  reduces the shelf life of produce by one day. This is why we have invested in
  the best in post-harvest technology, which includes hydro, vacuum and forced
  air cooling methods. And upon customer request, Ratto Bros. will “top-ice”
  produce boxes and pallets for shipment.”
  <strong>Do you have any cattle or livestock?</strong>
  No, and they never thought about it.
</p>
<h2 id="costs">Costs</h2>
<p>
  <strong>If you had 5,000 extra dollars, what would you do with it?</strong>
  As they are in the process of building a new facility, they would use this to support
  the operations for those.
</p>
<p>
  <strong>Do you have multiple streams of income?</strong>
  Nope, just growing and selling vegetables. In comparison to other farms who can
  have farm tours, education programs, onsight camping, and more to help ends meet.
</p>
<p>
  <strong>Where do you sell most of your goods?</strong>
  Ratto brothers sell primarily to wholesale distributors, who take mostly take their
  produce around California, but sometimes to the east cost. It seems they do not
  partake in farmers markets or local stands.
</p>
<p>
  <strong>Where do most of your costs come from?</strong>
  Frank says they come from &quot;buying synthetic inputs and pesticides, transportation,
  marketing, land/mortgage&quot;.
</p>
<h2 id="soil">Soil</h2>
<p>
  <strong
    >Was there ever a time in which the farm was challenged by its environments?</strong
  >
  Right now, agriculture is very challenging. Regulations are not agricultural friendly
  and rules are very stringent. Water is a problem, there isn&#39;t enough, the cost
  of labor is very high. Inputs such as gas, diesel, oil based chemicals are expensive,
  but necessary to transport goods.
</p>
<p>
  <strong
    >What kinds of tools do you use to measure the soil and what kinds of
    information do you get from it?</strong
  >
  Their farm takes weekly soil samples of sections of the land and send it to a lab
  to get it tested. The lab will tell them the nutrient profile of the soil sample,
  and Frank will respond by changing the type of fertilizer or inputs necessary to
  replace lost nutrients or balance the pH profile. This is a dynamic, manual process
  that dictates the outcome of the produce. They use a comination of commercial fertilizers,
  soil amendments, and organic matter to keep this fragile balance. Frank isn&#39;t
  sure what the lab does to test the soil.
</p>
<p>
  <strong
    >How would you rate the quality of your soil, and what do you think has
    contributed to this number?</strong
  >
  9/10 Frank gratefully acknowledges that they farm in a great area with very healthy
  soils.
</p>
<p>
  <strong>Do you know what regenerative agriculture is?</strong>
  He said &#39;no&#39;, and asked me to explain. I described it as a set of practices
  farmers use to bring life back into their soil, including crop rotations, maximization
  of biodiversity, and no-till methods. He countered by saying, &quot;whatever they
  do in organic practices, conventional ones are doing it too&quot;. Crop rotation,
  especially. From his perspective, all farmers are doing everything they can to
  produce the best products that meet the safety guidelines underlined by the FDA.
  They must do extensive testing to ensure this measure of quality on every harvest.
</p>
<p>
  side note: this makes me wonder how US regulation of food compares to that of
  the EU, where it is much stricter. Would we all enjoy much healthier,
  chemical-free food if the US made their regulations higher and farmers were
  forced to adapt? Or would that be too big a change for farmers to reach?
</p>
<p>
  <strong>Where do you get your water from?</strong>
  Water from the ground (through buried drip), aquafirs, river water, and canal water.
  They have a really good irrigation district near them where some of their water
  reserves come from.
</p>
<p>
  <strong>Do you till?</strong>
  Yes
</p>
